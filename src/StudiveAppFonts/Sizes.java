package StudiveAppFonts;

/**
 * Clase que contiene los tamaños de fuente predefinidos utilizados en la aplicación.
 * Estos tamaños son constantes y se emplean en distintos contextos dentro de la interfaz gráfica,
 * como títulos, subtítulos y párrafos, para mantener una apariencia consistente.
 */
public class Sizes {

    /** Tamaño de fuente para los títulos principales */
    public static float TitleSize = 50;

    /** Tamaño de fuente para los subtítulos o títulos secundarios */
    public static float MidTitleSize = 30;

    /** Tamaño de fuente para los subtítulos más pequeños */
    public static float SubtitleSize = 28;
}
